package chap_4;

public class chap_4_4_switch {

	public static void main(String[] asdf) {
		int rank = 2;
		
		switch(rank) {
		case 1: 
			System.out.println("AA");
			break;
		case 2: 
			System.out.println("BB");
			break;

		case 3: 
			System.out.println("CC");
			break;

		default: 
			System.out.println("DD");
			break;

				
		}
				
	}
}
